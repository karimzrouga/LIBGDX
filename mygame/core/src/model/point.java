package model;

public class point {
public point(int posx, int posy) {
		
		this.posx = posx;
		this.posy = posy;
	}
private int posx=0;
private int posy=0;
public int getPosx() {
	return posx;
}
public void setPosx(int posx) {
	this.posx = posx;
}
public int getPosy() {
	return posy;
}
public void setPosy(int posy) {
	this.posy = posy;
}
}
